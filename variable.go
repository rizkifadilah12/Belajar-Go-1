package main

import "fmt"

func main() {
	var name string

	name = "nama saya"
	fmt.Println(name)

	name = "nama saya adalah"
	fmt.Println(name)

	var othername = "budi"
	fmt.Println(othername)

	var age = 20
	fmt.Println(age)

	nama := "kamu"
	fmt.Println(nama)

	nama = "siapa"
	fmt.Println(nama)

	var (
		firstname = "budi"
		lastname  = "ono"
	)
	fmt.Println(firstname)
	fmt.Println(lastname)

}
